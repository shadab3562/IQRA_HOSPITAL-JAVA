    private void removeRowFromTable(Object rowData) {
        DefaultTableModel tableModel = (DefaultTableModel) transTable.getModel();
        for (int row = 0; row < tableModel.getRowCount(); row++) {
            if (tableModel.getValueAt(row, 0).equals(((Object[]) rowData)[0])) {
                tableModel.removeRow(row);
                break;
            }
        }
    }

    private void removeRowFromDatabase(Object rowData) {
        String transactionID = ((Object[]) rowData)[0].toString();
        try {
            // Establish database connection
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/iqrahospital", "root", "");
            String deleteStatement = "DELETE FROM transaction WHERE id = ?";
            PreparedStatement stmt = conn.prepareStatement(deleteStatement);
            stmt.setString(1, transactionID);
            stmt.executeUpdate();

            // Close the database connection
            stmt.close();
            conn.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex + "Error removing row from the database");
        }
    }

    class Node {

        Object data;
        Node next;

        Node(Object data) {
            this.data = data;
        }
    }

    class Stack {

        Node top;

        public void push(Object data) {
            Node newNode = new Node(data);
            newNode.next = top;
            top = newNode;
        }

        public Object pop() {
            if (!isEmpty()) {
                Object data = top.data;
                top = top.next;
                return data;
            }
            return null;
        }

        public boolean isEmpty() {
            return top == null;
        }

    }

