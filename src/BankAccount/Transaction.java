    private void pushActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pushActionPerformed
        if (dataPushed) {
            JOptionPane.showMessageDialog(null, "Data already pushed!");
            return;
        }

        try {
            // Establish database connection
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/iqrahospital", "root", "");
            String selectStatement = "SELECT * FROM transaction WHERE SenderNo = ? OR ReceiverNo = ?";
            PreparedStatement stmt = conn.prepareStatement(selectStatement);
            stmt.setInt(1, accountNo);
            stmt.setInt(2, accountNo);
            java.sql.ResultSet resultSet = stmt.executeQuery();

            // Check if the result set is empty
            if (!resultSet.isBeforeFirst()) {
                JOptionPane.showMessageDialog(null, "No data found in the database.");
            }

            // Process the retrieved data in a loop
            while (resultSet.next()) {
                String transactionID = resultSet.getString("id");
                String senNo = resultSet.getString("SenderNo");
                String senName = resultSet.getString("SenderName");
                String recNo = resultSet.getString("ReceiverNo");
                String recName = resultSet.getString("ReceiverName");
                String amount = resultSet.getString("Amount");
                String date = resultSet.getString("date");

                // Add data to the table model
                DefaultTableModel tableModel = (DefaultTableModel) transTable.getModel();
                Object[] rowData = {transactionID, senNo + " (" + senName + ")", recNo + " (" + recName + ")", amount + ".00", date};
                tableModel.insertRow(0, rowData); // Insert at the beginning
                stack.push(rowData); // Push the data onto the custom stack
            }

            // Close the database connection
            resultSet.close();
            stmt.close();
            conn.close();

            // Set the flag to indicate data has been pushed
            dataPushed = true;
        } catch (ClassNotFoundException | SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error connecting to the database");
        }
    }//GEN-LAST:event_pushActionPerformed

    private void popBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_popBtnActionPerformed
        if (!stack.isEmpty()) {
            Object rowData = stack.pop();
            removeRowFromTable(rowData);
            removeRowFromDatabase(rowData);
        } else {
            JOptionPane.showMessageDialog(null, "Stack is empty");
        }
    }//GEN-LAST:event_popBtnActionPerformed

